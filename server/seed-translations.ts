// Prepare statements
const selectTextId = db.prepare('SELECT id FROM texts WHERE key = ?');
const upsertTranslation = db.prepare('INSERT INTO translations (text_id, lang_code, value) VALUES (?, ?, ?) ON CONFLICT(text_id, lang_code) DO UPDATE SET value=excluded.value');

// Function to add translations for a language
function addTranslations(translations: Array<{key: string, value: string}>, langCode: string) {
  console.log(`Adding ${langCode} translations...`);
  
  for (const translation of translations) {
    const textRow = selectTextId.get(translation.key) as { id: number } | undefined;
    if (textRow) {
      upsertTranslation.run(textRow.id, langCode, translation.value);
    } else {
      console.warn(`Text key not found: ${translation.key}`);
    }
  }
  
  console.log(`Completed ${langCode} translations.`);
}

